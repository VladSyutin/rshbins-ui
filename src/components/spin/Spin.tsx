function joinClassNames(...values: Array<string | false | null | undefined>): string {
  return values.filter(Boolean).join(' ');
}

/**
 * Token-aware loading spinner translated from the Figma component set.
 */
export function Spin({
  className,
  label,
  size = '2xs',
  style,
  ...props
}: SpinProps) {
  const graphic = SPIN_GRAPHIC_MAP[size];
  const resolvedStyle = {
    ...style,
    '--rshb-spin-scale': String(SPIN_RATIO_MAP[size]),
    '--rshb-spin-size': `${SPIN_SIZE_MAP[size]}px`
  } as CSSProperties;

  return (
    <span
      {...props}
      aria-hidden={label ? undefined : props['aria-hidden'] ?? true}
      aria-label={label}
      aria-live={label ? 'polite' : undefined}
      className={joinClassNames('rshb-spin', className)}
      role={label ? 'status' : undefined}
      style={resolvedStyle}
    >
      <span aria-hidden="true" className="rshb-spin__inner">
        <svg
          aria-hidden="true"
          className="rshb-spin__svg"
          fill="none"
          viewBox={graphic.viewBox}
          xmlns="http://www.w3.org/2000/svg"
        >
          <path className="rshb-spin__path" d={graphic.path} fill="currentColor" />
        </svg>
      </span>
    </span>
  );
}
